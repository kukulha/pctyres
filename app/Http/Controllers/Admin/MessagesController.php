    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $message = Message::findOrfail($id)->delete();
        return back()->with('info', 'Mensaje eliminado correctamente');
    }
